// Student class
class Student {
    String name;
    int age;

    void display() {
        System.out.println("Name: " + name);
        System.out.println("Age : " + age);
    }
}

// Main class
public class Main {
    public static void main(String[] args) {

        Student s1 = new Student();   // object created

        s1.name = "Gnaneshwar";
        s1.age = 21;

        s1.display();   // method call
    }
}